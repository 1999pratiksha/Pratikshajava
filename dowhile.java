public class dowhile {
    public static void main(String[] args) {
        int i=0;
        while(i<=10){
            System.out.println(i*7);
            i++;
        }

        //dowhile loop program
        int j=0;
        do{
            System.out.println(j*7);
            j++;
        }while (j<=10);
    }
}
